<script setup lang="ts">
import { ref, reactive, watch } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { ElMessage } from 'element-plus'
import { useCounterStore } from '@/stores/counter'

  let route = useRoute()

  let store = useCounterStore()

  let router = useRouter()

const data = reactive({
  LeftArrDatas: [] as any,
  userInfo: {} as any,
  show: false
})
//把pinan里面的侧边栏数据取出来
data.LeftArrDatas = store.LeftArrDatas
data.userInfo = store.model
let SytemUrl = ref(route.meta.url)

//跳转页面
const SystemLists = (data: any) => {
  SytemUrl.value = '/SystemMenu' + data.url
  router.push({
    path: '/SystemMenu' + data.url
  })
}
//去出本地用户数据
const SignOut = () => {
  router.replace('/')
  location.reload()
  store.menus = []
  store.LeftArrDatas = []
  store.model = {}
  store.token = ''
  store.type = ''
  store.url = ''
  ElMessage({
    message: '退出登录成功',
    type: 'success'
  })
}
//跳转到设置密码页面
const setPass = () => {
  SytemUrl.value = '/SystemMenu/set'
  router.push('/SystemMenu/set')
  data.show = false
}
</script>

